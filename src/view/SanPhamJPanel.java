    private void tbViewSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbViewSPMouseClicked
        // TODO add your handling code here:
        txtMa.enable(false);
        int row = tbViewSP.getSelectedRow();
        String Ma =tbViewSP.getValueAt(row, 0).toString();
        String Ten =tbViewSP.getValueAt(row, 1).toString();
        String DVT =tbViewSP.getValueAt(row, 2).toString();
        String Sl =tbViewSP.getValueAt(row, 3).toString();
        txtGia.setText("");
        txtNgayCN.setText("");

        txtMa.setText(Ma);
        txtTen.setText(Ten);
        txtDvt.setText(DVT);
        txtSL.setText(Sl);
        
        DonGiaDAO da = new DonGiaDAO();
        List<DonGia> list1 = da.getDGbySP(Ma);
        DefaultTableModel model = (DefaultTableModel)tbViewDG.getModel();
        while(model.getRowCount()!=0){
            model.removeRow(0);
        }
        for(DonGia o : list1){
            model.addRow(new Object[]{
                o.getNgayCn(),
                o.getGia()
            });
        }
        btnAdd.setEnabled(false);
        btnDele.setEnabled(true);
        btnUpd.setEnabled(true);
    }//GEN-LAST:event_tbViewSPMouseClicked

