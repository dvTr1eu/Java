    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        String Ma=txtMa.getText();
        String Ten=txtTen.getText();
        String DVT=txtDvt.getText();
        int Sl=Integer.parseInt(txtSL.getText());
        String Bday = txtNgayCN.getText();
        float Gia = Float.parseFloat(txtGia.getText());
        DonGiaDAO donGiaDAO = new DonGiaDAO();
        SanPhamDAO da = new SanPhamDAO();
        if(da.them(Ma, Ten, DVT, Sl) && donGiaDAO.themDon(Ma, Gia, Bday)){
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            reset();
        }
        else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
        List<SanPham> list = da.getList();
        DefaultTableModel model = (DefaultTableModel)tbViewSP.getModel();
        while(model.getRowCount()!=0){
            model.removeRow(0);
        }
        for(SanPham o : list){
            model.addRow(new Object[]{
                o.getMasp(),
                o.getTensp(),
                o.getDonvitinh(),
                o.getSoluong()
            });
        }
    }//GEN-LAST:event_btnAddActionPerformed

