    public void fillTable(){
        List<HoaDon> list = hoaDonDAO.getList();
        DefaultTableModel model = (DefaultTableModel)tbView.getModel();
        while(model.getRowCount()!=0){
             model.removeRow(0);
         }
        for(HoaDon o : list){
            model.addRow(new Object[]{
            o.getMahd(),
            o.getMakh(),
            o.getManv(),
            o.getNgaylap(),
            o.getThanhtoan()
        });
        }
    }

