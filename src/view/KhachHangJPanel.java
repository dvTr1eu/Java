    public void fillTable(){
        List<KhachHang> list = khachHangDAO.getList();
        DefaultTableModel dtm = (DefaultTableModel)tbView.getModel();
        while(dtm.getRowCount()!=0){
             dtm.removeRow(0);
         }
        for(KhachHang ob : list){
            Object rowData[] = new Object[]{
                ob.getMakh(),
                ob.getHoten(),
                ob.getDiachi(),
                ob.getSodt()
            };
            dtm.addRow(rowData);
        }
    }

