    private void tbViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbViewMouseClicked

        int selectedRow = tbView.getSelectedRow();  
        String manv = (String) tbView.getValueAt(selectedRow, 0);
        if(selectedRow >= 0){
            for(NhanVien nhanVien : list){
                if(manv.equals(nhanVien.getManv())){
                    txtMaNv.setText(manv);
                    txtMaNv.enable(false);
                    txtTenNv.setText(nhanVien.getTennv());
                    txtNgaySinh.setText(nhanVien.getNgaysinh().toString());
                    txtGioiTinh.setText(nhanVien.getGioitinh());
                    txtSDT.setText(nhanVien.getSodienthoai());
                    txtDiaChi.setText(nhanVien.getDiachi());
                    break;
                }
            }
        }
        btnAdd.enableInputMethods(false);
    }//GEN-LAST:event_tbViewMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String ten = txtTim.getText();
        if(ten.isEmpty()){
            JOptionPane.showMessageDialog(this, "Hãy nhập tên nhân viên cần tìm");
        }
        else{
            try {
                String sql = "select * from NhanVien where HoTen like N'%"+ten+"%'";
                List<NhanVien> findList = new ArrayList<>();
                PreparedStatement pst = conn.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                while(rs.next()){
                    nhanVien.setManv(rs.getString("MaNV"));
                    nhanVien.setTennv(rs.getString("HoTen"));
                    nhanVien.setNgaysinh(rs.getDate("NgaySinh"));
                    nhanVien.setGioitinh(rs.getString("GioiTinh"));
                    nhanVien.setSodienthoai(rs.getString("SoDT"));                    
                    nhanVien.setDiachi(rs.getString("DiaChi"));
                    findList.add(nhanVien);
                }
                if(findList.size() > 0){
                    DefaultTableModel dtm = (DefaultTableModel)tbView.getModel();
                    dtm.setRowCount(0);
                    for(NhanVien nhanVien : findList){
                        Object rowData[] = new Object[]{
                            nhanVien.getManv(),
                            nhanVien.getTennv(),
                            nhanVien.getNgaysinh(),
                            nhanVien.getGioitinh(),
                            nhanVien.getSodienthoai(),
                            nhanVien.getDiachi()
                        };
                        dtm.addRow(rowData);
                    }
                }
                else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên");
                } 
            }
            catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed


