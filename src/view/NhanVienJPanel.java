    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed

        int removeIndex = tbView.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel)tbView.getModel();
        if(removeIndex == -1){
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
        }else if(list.size() == 0){
            JOptionPane.showMessageDialog(this, "Không có nhân viên để xóa");
        }else {
            String sql = "Delete from NhanVien where MaNV = ?";
            try {
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, tbView.getValueAt(tbView.getSelectedRow(), 0).toString());
                if(JOptionPane.showConfirmDialog(this, "Bạn muốn xóa nhân viên này ?","Confirm",
                        JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                    pst.executeUpdate();
                    dtm.setRowCount(0);
                    list.remove(removeIndex);
                    removeIndex--;
                    fillTable();
                    reset();
                }
            }
            catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

